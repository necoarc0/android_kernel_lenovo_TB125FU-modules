/*
 * osal_typedef.h - OS Abstraction Layer Type Definitions
 */

#ifndef __OSAL_TYPEDEF_H__
#define __OSAL_TYPEDEF_H__

#include <linux/types.h>

/* Define common types used in the OS Abstraction Layer */
typedef uint8_t u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;

typedef int8_t s8;
typedef int16_t s16;
typedef int32_t s32;
typedef int64_t s64;

#endif /* __OSAL_TYPEDEF_H__ */